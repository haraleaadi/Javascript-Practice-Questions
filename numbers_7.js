// display -10 to -1 using for loop :

function number(){
    for(let i=-10; i<=-1; i++)
    {
        console.log(i)
    }
}
number()

//Using while loop

function reverse(){
let i = -10;
while(i <= -1)
{
    console.log(i)
    i++;
}
}
reverse()

