// print first 10 natural using while loop :

function natural(){
    let i=1;
    while(i<=10)
    {
        console.log(i)
        i++;
    }
}
natural()

//Using for loop 

function numbers(){
    for(let i=1; i<=10; i++)
    {
        console.log(i)
    }
}
numbers()

//Using do while loop

function numberList(n){
    let i=1;
    do{
        console.log(i)
        i++;
    }
    while(i<=n)
}

numberList(10)



